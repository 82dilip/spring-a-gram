package com.greglturnquist.springagram.backend;

import static org.springframework.amqp.core.BindingBuilder.*;

import org.springframework.amqp.core.Binding;
import org.springframework.amqp.core.Queue;
import org.springframework.amqp.core.TopicExchange;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

/**
 * @author Greg Turnquist
 */
// tag::code[]
@Configuration
public class RabbitConfig {

	public static final String QUEUE = "spring-a-gram";
	public static final String EXCHANGE = "spring-a-gram-exchange";

	@Bean
	public Queue queue() {
		return new Queue(QUEUE, false);
	}

	@Bean
	public TopicExchange exchange() {
		return new TopicExchange(EXCHANGE);
	}

	@Bean
	public Binding binding(Queue queue, TopicExchange exchange) {
		return bind(queue).to(exchange).with("backend.*");
	}

}
// end::code[]
